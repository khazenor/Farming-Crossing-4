const isLetsDo = (block) => (letsDoStations.indexOf(block.id) !== -1)

BlockEvents.placed(event => {
  const { block, player } = event

  if (isLetsDo(block.down) && isBotanyBot(block)) {
    player.tell("Please don't place botany pots on top of Let's Do workstations. It causes the world to crash.")
    event.cancel()
  }
  if (isLetsDo(block) && isBotanyBot(block.up)) {
    player.tell("Please don't place Let's Do workstations below botany pots. It causes the world to crash.")
    event.cancel()
  }
});